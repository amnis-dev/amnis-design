export interface LoaderSpinProps {
  /**
   * Size of the loader.
   * @default "0.8em"
   */
  size?: string;

  /**
    * Animation duration in seconds.
    * @default 0.8
    */
  duration?: number;

  /**
   * The thickness of the spinner ring.
   * @default "0.2em"
   */
  thickness?: string;
}
